def area(a, h):
    '''
    Вычисляет площадь треугольника.
    
    Параметры:
    a (float): длина основания треугольника
    h (float): высота треугольника, проведенная к основанию
    
    Возвращаемое значение:
    float: площадь треугольника по формуле S = (a × h) / 2
    
    Пример вызова:
    area(6, 4)  # возвращает 12.0
    '''
    return a * h / 2

def perimeter(a, b, c):
    '''
    Вычисляет периметр треугольника.
    
    Параметры:
    a (float): длина первой стороны
    b (float): длина второй стороны
    c (float): длина третьей стороны
    
    Возвращаемое значение:
    float: периметр треугольника по формуле P = a + b + c
    
    Пример вызова:
    perimeter(3, 4, 5)  # возвращает 12
    '''
    return a + b + c